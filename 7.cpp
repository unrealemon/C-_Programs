#include<bits/stdc++.h>
using namespace std;

int sroot(int a)
{
    return sqrt(a);
}

long int sroot(long int a)
{
    return sqrt(a);
}
double sroot(double a)
{
    return sqrt(a);
}



int main()
{
    int x = 100 ;
    cout<<"Sroot For integer number: "<<sroot(x)<<endl;

    long int y = 262144 ;
    cout<<"Sroot For integer number: "<<sroot(y)<<endl;

    double z = 550 ;
    cout<<"Sroot For integer number: "<<sroot(z)<<endl;

    return 0;
}
